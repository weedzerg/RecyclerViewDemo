public class HeroObject {
    private String idAvatar;
    private String name;

    public HeroObject(String idAvatar, String name) {
        this.idAvatar = idAvatar;
        this.name = name;
    }

    public String getIdAvatar() {
        return idAvatar;
    }

    public String getName() {
        return name;
    }
}
